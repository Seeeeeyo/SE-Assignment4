
public class Ellispe {
	public double cx;
	public double cy;
	public double rx;
	public double ry;
	public Ellispe()
	{
		
	}
	
	public void setcx(double cx)
	{
		this.cx = cx;
	}
	public void setcy(double cy)
	{
		this.cy = cy; 
	}

	public void setrx(double rx)
	{
		this.rx = rx;
	}
	public void setry(double ry)
	{
		this.ry = ry; 
	}
	public double getcx()
	{
		return cx;
	}
	public double getcy()
	{
		return cy;
	}
	public double getrx()
	{
		return rx;
	}
	public double getry()
	{
		return ry;
	}
	

}
